public class SumOfNumbers {
    public static void main(String[] args) {
        int sum = 0;

        // Use a for loop to calculate the sum of numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }

        System.out.println("Sum of numbers from 1 to 10: " + sum);
    }
}
